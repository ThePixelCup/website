import { useState, useEffect } from 'react';
import { useSDK } from "@thirdweb-dev/react";

function useEthPrice() {
  const [price, setPrice] = useState(1500);
  const sdk = useSDK();
  const aggregatorV3InterfaceABI = [
  {
    inputs: [],
    name: 'decimals',
    outputs: [{ internalType: 'uint8', name: '', type: 'uint8' }],
    stateMutability: 'view',
    type: 'function'
  },
  {
    inputs: [],
    name: 'description',
    outputs: [{ internalType: 'string', name: '', type: 'string' }],
    stateMutability: 'view',
    type: 'function'
  },
  {
    inputs: [{ internalType: 'uint80', name: '_roundId', type: 'uint80' }],
    name: 'getRoundData',
    outputs: [
      { internalType: 'uint80', name: 'roundId', type: 'uint80' },
      { internalType: 'int256', name: 'answer', type: 'int256' },
      { internalType: 'uint256', name: 'startedAt', type: 'uint256' },
      { internalType: 'uint256', name: 'updatedAt', type: 'uint256' },
      { internalType: 'uint80', name: 'answeredInRound', type: 'uint80' }
    ],
    stateMutability: 'view',
    type: 'function'
  },
  {
    inputs: [],
    name: 'latestRoundData',
    outputs: [
      { internalType: 'uint80', name: 'roundId', type: 'uint80' },
      { internalType: 'int256', name: 'answer', type: 'int256' },
      { internalType: 'uint256', name: 'startedAt', type: 'uint256' },
      { internalType: 'uint256', name: 'updatedAt', type: 'uint256' },
      { internalType: 'uint80', name: 'answeredInRound', type: 'uint80' }
    ],
    stateMutability: 'view',
    type: 'function'
  },
  {
    inputs: [],
    name: 'version',
    outputs: [{ internalType: 'uint256', name: '', type: 'uint256' }],
    stateMutability: 'view',
    type: 'function'
  }];
  useEffect(() => {
    async function getPrice() {
      // The address of the contract which will provide the price of ETH
      const priceFeed = await sdk.getContractFromAbi(
        process.env.GATSBY_PRICE_FEED,
        aggregatorV3InterfaceABI
      );
      const roundData = await priceFeed.call('latestRoundData');
      const decimals = await priceFeed.call('decimals');
      const priceNumber = Number((roundData.answer.toString() / Math.pow(10, decimals)).toFixed(2));
      setPrice(priceNumber);
    }
    if (sdk) {
      getPrice();
    }
  });
  
  return price;
};

export default useEthPrice;